// ============================================================================
// src/components/AuthLayout.tsx - "СЦЕНА" ДЛЯ ФОРМ АВТОРИЗАЦИИ
// ============================================================================
// Это очень простой, но очень полезный компонент-обертка. Его единственная
// задача - создать фон и разместить любой дочерний контент (например,
// форму входа) идеально по центру экрана.
// ============================================================================

// --- 1. ИМПОРТЫ ---

import { ReactNode } from "react";
// ReactNode - это специальный тип из React, который говорит:
// "этот компонент может принимать в себя любой валидный React-элемент
// (другой компонент, текст, html-теги и т.д.)".

// ============================================================================
// --- КОМПОНЕНТ AuthLayout ---
// ============================================================================

// Компонент принимает один пропс - `children`.
// `children` - это специальное имя для всего, что мы вкладываем
// ВНУТРЬ тегов нашего компонента. Например: <AuthLayout>СЮДА</AuthLayout>.
export default function AuthLayout({ children }: { children: ReactNode }) {
  return (
    // Это и есть наша "сцена".
    <div className="min-h-screen flex items-center justify-center bg-gray-50 px-4">
      {/*
        Разбор "магии" центрирования с помощью Tailwind CSS:
        - min-h-screen:  Делает контейнер высотой во весь экран.
        - flex:          Включает режим Flexbox.
        - items-center:  Центрирует содержимое по вертикали.
        - justify-center: Центрирует содержимое по горизонтали.
        - bg-gray-50:    Задает приятный светло-серый фон.
        - px-4:          Добавляет небольшие отступы по бокам для мобильных устройств.
      */}

      {/* А сюда, в центр "сцены", мы помещаем наших "актёров" - дочерние элементы. */}
      {children}
    </div>
  );
}
